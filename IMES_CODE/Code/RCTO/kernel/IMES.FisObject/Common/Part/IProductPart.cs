using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace IMES.FisObject.Common.Part
{
    /// <summary>
    /// 与Product/MB绑定的Part
    /// </summary>
    public interface IProductPart
    {
        /// <summary>
        /// 记录标识
        /// </summary>
        int ID { get; set;  }

        /// <summary>
        /// Product ID/MB Sn
        /// </summary>
        string ProductID { get; set; }

        /// <summary>
        /// Pn
        /// </summary>
        string PartID { get; set; }

        /// <summary>
        /// Part Sn
        /// </summary>
        string PartSn { get; set; }

        /// <summary>
        /// Iecpn
        /// </summary>
        string Iecpn { get; set; }

        /// <summary>
        /// CustomerPn
        /// </summary>
        string CustomerPn { get; set; }

        /// <summary>
        /// BOMNode Type
        /// </summary>
        string PartType { get; set; }

        /// <summary>
        /// 维护人员
        /// </summary>
        string Editor { get; set; }

        /// <summary>
        /// Station
        /// </summary>
        string Station { get; set; }

        /// <summary>
        /// 创建时间
        /// </summary>
        DateTime Cdt { get; set; }

        /// <summary>
        /// 更新时间
        /// </summary>
        DateTime Udt { get; set; }

        string Value { get; set; }
        
        [Obsolete]
        string ValueType { get; set; }

        string CheckItemType { get; set; }

        string BomNodeType { get; set; }
    }
}
